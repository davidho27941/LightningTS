import numpy as np
import pandas as pd

from typing import List

from pandas.tseries import offsets
from pandas.tseries.frequencies import to_offset

from deprecated import deprecated


class TimeEncoding:
    """A class contains several static method. These methods can help
    in loading/encoding datasets.
    """

    @deprecated(version="0.1.0")
    @staticmethod
    def get_data_dir(dataset_name):
        data_info = {}

    @deprecated(version="0.1.0")
    @staticmethod
    def load_dataset(
        dataset_name: str,
        data_dir: str,
        format: str = "csv",
        multi_file: bool = False,
    ):
        """Load datasets with variation of file type."""
        match [multi_file, format]:
            case [False, "csv"]:
                data = pd.read_csv(data_dir)
            case [False, "parquet"]:
                data = pd.read_parquet(data_dir)
            case _:
                raise ValueError("The choice is not available.")
        return data

    @staticmethod
    def time_features(dates, freq="h"):
        """Function to encode the time series proposed used by autoformer's paper."""
        return np.vstack(
            [
                feature_encoder(dates)
                for feature_encoder in time_features_from_frequency_str(freq)
            ]
        )


